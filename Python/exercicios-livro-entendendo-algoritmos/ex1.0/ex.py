# 1.1 Suponha que você tenha uma lista com 128 nomes e esteja fazendo uma pesquisa binária. Qual seria o
# número máximo de etapas que levaria para encontrar o número desejado?

# Pesquisa simpples: 128 etapas necessárias

# Pesquisa binária: log128 = 2 elevado a 7



# 1.2 Suponha que você duplique o tamanho da lista. Qual seria o número máximo de etapas agora?

# Pesquisa simpples: 256 etapas necessárias

# Pesquisa binária: log256 = 2 elevado a 8
